const STORAGE_KEY = "siteLangV2";

const updateToggleLabel = (lang) => {
  const label = lang === "ko" ? "English" : "Korean";
  const toggles = document.querySelectorAll("[data-lang-toggle]");

  toggles.forEach((toggle) => {
    const text = toggle.querySelector(".lang-text");

    if (text) {
      text.textContent = label;
      return;
    }

    toggle.textContent = label;
  });
};

const applyLanguage = (lang) => {
  const current = translations[lang] || translations.en;

  document.documentElement.lang = lang;

  document.querySelectorAll("[data-i18n], [data-i18n-html]").forEach((element) => {
    const key = element.dataset.i18n || element.dataset.i18nHtml;
    const value = current[key];

    if (value === undefined || value === null) {
      return;
    }

    const attr = element.dataset.i18nAttr;

    if (attr) {
      element.setAttribute(attr, value);
      return;
    }

    if (element.dataset.i18nHtml !== undefined) {
      element.innerHTML = value;
    } else {
      element.textContent = value;
    }
  });

  updateToggleLabel(lang);
  if (typeof setupTypingEffect === "function") {
    setupTypingEffect();
  }
  localStorage.setItem(STORAGE_KEY, lang);
};

const setupLanguageToggle = () => {
  const toggles = document.querySelectorAll("[data-lang-toggle]");

  if (!toggles.length) {
    return;
  }

  toggles.forEach((toggle) => {
    toggle.addEventListener("click", () => {
      const currentLang = document.documentElement.lang || "en";
      const nextLang = currentLang === "ko" ? "en" : "ko";
      applyLanguage(nextLang);
    });
  });
};

const getInitialLanguage = () => {
  const saved = localStorage.getItem(STORAGE_KEY);

  if (saved && translations[saved]) {
    return saved;
  }

  return "en";
};

// Export globals for init.js
window.applyLanguage = applyLanguage;
window.setupLanguageToggle = setupLanguageToggle;
window.getInitialLanguage = getInitialLanguage;
