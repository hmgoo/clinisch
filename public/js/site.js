const blockUserActions = () => {
  const blockedEvents = [
    "copy",
    "cut",
    "paste",
    "contextmenu",
    "dragstart",
    "selectstart"
  ];

  blockedEvents.forEach((eventName) => {
    document.addEventListener(eventName, (event) => {
      event.preventDefault();
    });
  });

  document.querySelectorAll("img").forEach((image) => {
    image.setAttribute("draggable", "false");
  });
};

const getHeaderOffset = () => {
  const header = document.querySelector(".site-header");

  if (!header) {
    return 0;
  }

  return header.getBoundingClientRect().height;
};

const setupSmoothScroll = () => {
  document.querySelectorAll('a[href^="#"]').forEach((link) => {
    link.addEventListener("click", (event) => {
      const hash = link.getAttribute("href");

      if (!hash || hash === "#") {
        return;
      }

      const target = document.querySelector(hash);

      if (!target) {
        return;
      }

      event.preventDefault();

      const headerOffset = getHeaderOffset();
      const targetOffset = target.getBoundingClientRect().top + window.pageYOffset;
      const finalOffset = Math.max(targetOffset - headerOffset - 12, 0);

      window.scrollTo({
        top: finalOffset,
        behavior: "smooth"
      });
    });
  });
};

const updateToggleLabel = (lang) => {
  const toggle = document.querySelector("[data-lang-toggle]");

  if (!toggle) {
    return;
  }

  toggle.textContent = lang === "ko" ? "EN" : "KO";
};

const applyLanguage = (lang) => {
  const current = translations[lang] || translations.ko;

  document.documentElement.lang = lang;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    const value = current[key];

    if (!value) {
      return;
    }

    const attr = element.dataset.i18nAttr;

    if (attr) {
      element.setAttribute(attr, value);
      return;
    }

    element.textContent = value;
  });

  updateToggleLabel(lang);
  localStorage.setItem("siteLang", lang);
};

const setupLanguageToggle = () => {
  const toggle = document.querySelector("[data-lang-toggle]");

  if (!toggle) {
    return;
  }

  toggle.addEventListener("click", () => {
    const currentLang = document.documentElement.lang || "ko";
    const nextLang = currentLang === "ko" ? "en" : "ko";
    applyLanguage(nextLang);
  });
};

const getInitialLanguage = () => {
  const saved = localStorage.getItem("siteLang");

  if (saved && translations[saved]) {
    return saved;
  }

  return "ko";
};

document.addEventListener("DOMContentLoaded", () => {
  renderHeader();
  renderFooter();
  blockUserActions();
  setupSmoothScroll();

  const initialLanguage = getInitialLanguage();
  applyLanguage(initialLanguage);
  setupLanguageToggle();
});
